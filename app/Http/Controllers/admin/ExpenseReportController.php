    public function Search_Expense(Request $request){

        $rdio = $request->rdio;


        // في حالة البحث بنوع الفاتورة

        if ($rdio == 1) {


            // في حالة عدم تحديد تاريخ
            if ($request->start_at =='' && $request->end_at =='') {

                $Expense = Expense::get();
                return view('admin.reports.expense_report')->withDetails($Expense);
            }

            // في حالة تحديد تاريخ استحقاق
            else {

                $start_at = date($request->start_at);
                $end_at = date($request->end_at);
                $type = $request->type;

                $Expense = Expense::whereBetween('expense_date',[$start_at,$end_at])
                    ->get();
                return view('admin.reports.expenses_report',compact('type','start_at','end_at'))->withDetails($Expense);

            }



        }

        //====================================================================

        // في البحث برقم الفاتورة
        else {

            $Expense = Expense::select('*')->where('expenses_name','=',$request->expenses_name)->get();
            return view('admin.reports.expenses_report')->withDetails($Expense);

        }



    }


